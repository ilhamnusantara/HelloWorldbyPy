#f digunakan untuk di belakang koma menentukan sampai berapa koma
print("Pi penjabaran {0:12}".format(22/7))
print("Pi penjabaran {0:12f}".format(22/7))
print("Pi penjabaran {0:12.50f}".format(22/7))
print("Pi penjabaran {0:52.50f}".format(22/7))
print("Pi penjabaran {0:1.2f}".format(22/7))

